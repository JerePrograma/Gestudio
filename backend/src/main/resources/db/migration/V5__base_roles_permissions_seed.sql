        WHEN 'RECEPCION' THEN 'Recepción'
        WHEN 'COBRANZAS' THEN 'Cobranzas'
        WHEN 'PROFESOR' THEN 'Profesor'
        WHEN 'LECTURA' THEN 'Lectura'
