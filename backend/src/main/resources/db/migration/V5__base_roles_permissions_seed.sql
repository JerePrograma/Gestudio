CREATE TABLE public.permisos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo VARCHAR(100) NOT NULL,
    descripcion VARCHAR(255) NOT NULL,
    modulo VARCHAR(50) NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    sistema BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_permisos_codigo UNIQUE (codigo)
);

ALTER TABLE public.roles
    ADD COLUMN codigo VARCHAR(50),
    ADD COLUMN nombre VARCHAR(100),
    ADD COLUMN descripcion_funcional VARCHAR(255),
    ADD COLUMN sistema BOOLEAN NOT NULL DEFAULT FALSE,
    ADD COLUMN editable BOOLEAN NOT NULL DEFAULT TRUE;

UPDATE public.roles
SET codigo = upper(descripcion),
    nombre = descripcion
WHERE codigo IS NULL OR nombre IS NULL;

ALTER TABLE public.roles
    ALTER COLUMN codigo SET NOT NULL,
    ALTER COLUMN nombre SET NOT NULL;

CREATE UNIQUE INDEX uq_roles_codigo_normalizado ON public.roles (upper(codigo));

CREATE TABLE public.usuario_roles (
    usuario_id BIGINT NOT NULL,
    rol_id BIGINT NOT NULL,
    asignado_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    asignado_por_usuario_id BIGINT,
    CONSTRAINT pk_usuario_roles PRIMARY KEY (usuario_id, rol_id),
    CONSTRAINT fk_usuario_roles_usuario FOREIGN KEY (usuario_id)
        REFERENCES public.usuarios(id) ON DELETE RESTRICT,
    CONSTRAINT fk_usuario_roles_rol FOREIGN KEY (rol_id)
        REFERENCES public.roles(id) ON DELETE RESTRICT,
    CONSTRAINT fk_usuario_roles_asignado_por FOREIGN KEY (asignado_por_usuario_id)
        REFERENCES public.usuarios(id) ON DELETE RESTRICT
);

CREATE INDEX ix_usuario_roles_rol ON public.usuario_roles (rol_id);
CREATE INDEX ix_usuario_roles_asignado_por ON public.usuario_roles (asignado_por_usuario_id);

CREATE TABLE public.rol_permisos (
    rol_id BIGINT NOT NULL,
    permiso_id BIGINT NOT NULL,
    CONSTRAINT pk_rol_permisos PRIMARY KEY (rol_id, permiso_id),
    CONSTRAINT fk_rol_permisos_rol FOREIGN KEY (rol_id)
        REFERENCES public.roles(id) ON DELETE RESTRICT,
    CONSTRAINT fk_rol_permisos_permiso FOREIGN KEY (permiso_id)
        REFERENCES public.permisos(id) ON DELETE RESTRICT
);

CREATE INDEX ix_rol_permisos_permiso ON public.rol_permisos (permiso_id);

