CREATE TABLE public.cargo_liquidaciones (
    cargo_id BIGINT PRIMARY KEY,
    periodo_desde DATE NOT NULL,
    tarifa_disciplina_id BIGINT,
    condicion_inscripcion_id BIGINT,
    origen_precio VARCHAR(30) NOT NULL,
    importe_base NUMERIC(19,2) NOT NULL,
    descuento_porcentaje NUMERIC(7,4) NOT NULL DEFAULT 0,
    descuento_importe NUMERIC(19,2) NOT NULL DEFAULT 0,
    recargo_porcentaje NUMERIC(7,4) NOT NULL DEFAULT 0,
    recargo_importe NUMERIC(19,2) NOT NULL DEFAULT 0,
    importe_final NUMERIC(19,2) NOT NULL,
    formula_version INTEGER NOT NULL,
    observaciones VARCHAR(500),
    calculada_por_usuario_id BIGINT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_cargo_liquidacion_origen CHECK (origen_precio IN (
        'TARIFA_HISTORICA', 'COSTO_PARTICULAR', 'MANUAL_HISTORICO', 'MIGRADO_CARGO_EXISTENTE'
    )),
    CONSTRAINT ck_cargo_liquidacion_importes CHECK (
        importe_base >= 0
        AND descuento_porcentaje BETWEEN 0 AND 100
        AND descuento_importe >= 0
        AND recargo_porcentaje BETWEEN 0 AND 100
        AND recargo_importe >= 0
        AND importe_final >= 0
    ),
    CONSTRAINT fk_liquidacion_cargo FOREIGN KEY (cargo_id)
        REFERENCES public.cargos(id) ON DELETE RESTRICT,
    CONSTRAINT fk_liquidacion_tarifa FOREIGN KEY (tarifa_disciplina_id)
        REFERENCES public.disciplina_tarifas(id) ON DELETE RESTRICT,
    CONSTRAINT fk_liquidacion_condicion FOREIGN KEY (condicion_inscripcion_id)
        REFERENCES public.inscripcion_condiciones_economicas(id) ON DELETE RESTRICT,
    CONSTRAINT fk_liquidacion_usuario FOREIGN KEY (calculada_por_usuario_id)
        REFERENCES public.usuarios(id) ON DELETE RESTRICT
);

CREATE INDEX ix_liquidaciones_tarifa ON public.cargo_liquidaciones (tarifa_disciplina_id);
CREATE INDEX ix_liquidaciones_condicion ON public.cargo_liquidaciones (condicion_inscripcion_id);
CREATE INDEX ix_liquidaciones_usuario ON public.cargo_liquidaciones (calculada_por_usuario_id);

CREATE TABLE public.cargo_eventos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cargo_id BIGINT NOT NULL,
    tipo VARCHAR(30) NOT NULL,
    estado_anterior VARCHAR(10),
    estado_nuevo VARCHAR(10),
    saldo_anterior NUMERIC(19,2),
    saldo_nuevo NUMERIC(19,2),
    referencia_tipo VARCHAR(30),
    referencia_id BIGINT,
    idempotency_key VARCHAR(150) NOT NULL,
    usuario_id BIGINT,
    ocurrido_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    correlation_id UUID,
    metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
    CONSTRAINT uq_cargo_evento_idempotency UNIQUE (idempotency_key),
    CONSTRAINT ck_cargo_evento_tipo CHECK (tipo IN (
        'EMITIDO', 'PAGO_APLICADO', 'PAGO_REVERTIDO', 'CREDITO_APLICADO',
        'CREDITO_REVERTIDO', 'RECARGO_CREADO', 'ANULADO'
    )),
    CONSTRAINT fk_cargo_evento_cargo FOREIGN KEY (cargo_id)
        REFERENCES public.cargos(id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargo_evento_usuario FOREIGN KEY (usuario_id)
        REFERENCES public.usuarios(id) ON DELETE RESTRICT
);

CREATE INDEX ix_cargo_eventos_cargo_fecha ON public.cargo_eventos (cargo_id, ocurrido_at, id);
CREATE INDEX ix_cargo_eventos_usuario ON public.cargo_eventos (usuario_id);

