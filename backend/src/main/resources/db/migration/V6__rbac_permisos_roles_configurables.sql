CREATE OR REPLACE FUNCTION public.impedir_cambio_codigo_roles()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.codigo <> OLD.codigo THEN
        RAISE EXCEPTION 'roles.codigo es inmutable' USING ERRCODE = '55000';
    END IF;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_roles_codigo_inmutable
BEFORE UPDATE ON public.roles
FOR EACH ROW EXECUTE FUNCTION public.impedir_cambio_codigo_roles();

