ALTER TABLE public.roles
    ADD COLUMN codigo VARCHAR(50),
    ADD COLUMN nombre VARCHAR(100),
    ADD COLUMN descripcion_funcional VARCHAR(255),
    ADD COLUMN sistema BOOLEAN NOT NULL DEFAULT FALSE,
    ADD COLUMN editable BOOLEAN NOT NULL DEFAULT TRUE;

UPDATE public.roles
SET codigo = upper(descripcion),
    nombre = descripcion,
    descripcion_funcional = descripcion,
    sistema = upper(descripcion) IN ('SUPERADMIN', 'ADMINISTRADOR'),
    editable = upper(descripcion) NOT IN ('SUPERADMIN');

ALTER TABLE public.roles
    ALTER COLUMN codigo SET NOT NULL,
    ALTER COLUMN nombre SET NOT NULL;

