    ADD CONSTRAINT uq_roles_codigo UNIQUE (codigo),
    ADD CONSTRAINT ck_roles_codigo_formato CHECK (codigo ~ '^[A-Z][A-Z0-9_]{2,49}$');

CREATE TABLE public.permisos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo VARCHAR(100) NOT NULL,
    descripcion VARCHAR(255) NOT NULL,
    modulo VARCHAR(50) NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE,
    sistema BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_permisos_codigo UNIQUE (codigo),
    CONSTRAINT ck_permisos_codigo_formato CHECK (codigo ~ '^PERM_[A-Z0-9_]{3,95}$')
);

CREATE TABLE public.rol_permisos (
    rol_id BIGINT NOT NULL,
    permiso_id BIGINT NOT NULL,
    CONSTRAINT pk_rol_permisos PRIMARY KEY (rol_id, permiso_id),
    CONSTRAINT fk_rol_permisos_rol FOREIGN KEY (rol_id) REFERENCES public.roles(id) ON DELETE CASCADE,
    CONSTRAINT fk_rol_permisos_permiso FOREIGN KEY (permiso_id) REFERENCES public.permisos(id) ON DELETE RESTRICT
);

CREATE INDEX ix_rol_permisos_permiso ON public.rol_permisos (permiso_id);

CREATE TABLE public.usuario_roles (
    usuario_id BIGINT NOT NULL,
    rol_id BIGINT NOT NULL,
    CONSTRAINT pk_usuario_roles PRIMARY KEY (usuario_id, rol_id),
    CONSTRAINT fk_usuario_roles_usuario FOREIGN KEY (usuario_id) REFERENCES public.usuarios(id) ON DELETE CASCADE,
    CONSTRAINT fk_usuario_roles_rol FOREIGN KEY (rol_id) REFERENCES public.roles(id) ON DELETE RESTRICT
);

CREATE INDEX ix_usuario_roles_rol ON public.usuario_roles (rol_id);

